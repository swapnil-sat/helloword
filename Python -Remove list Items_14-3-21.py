# >>>Remove Specified Item
# The remove() method removes the specified items.
# E.x
# Remove "banana" from the given list
# thislist=["apple","banana","cherry"]
# thislist.remove("banana")
# print(thislist)
# >>>remove a specific Index<<<<<<
# The pop() method removes the specific index.
# E.x
# thislist=["apple","banana","cherry"]
# thislist.pop(1)
# print(thislist)
# if you do not specify the index ,the pop() method removes the last item.
# E.x
# Remove the last item:
# thislist=["apple","banana","cherry"]
# thislist.pop()
# print(thislist)
# >>>the del()keyword also removes the specific index:
# E.x
# Remove the first item:
# thislist=["apple","cherry","banana"]
# del thislist[0]
# print(thislist)
# the del() keyword can also delet the list completely.
# E.x
# e=Delete the entire list
# thislist=["apple","cherry","banana"]
# del thislist
# print(thislist)
# >>>>>Clear the list.
# the clear() method empties the list.
# the list still remian but it has no containt.
# E.x
# thislist=["apple","banana","cherry"]
