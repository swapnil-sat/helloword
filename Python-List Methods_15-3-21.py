# >>List Methods.
# python has a set of built-in -methods that you can use on list.
# METHOD                                        DESCRIPTION
# 1.append()            Adds an element at the end of the list.
# 2.clear()             removes all the element from the list.
# 3.copy()              Returns a copy of  the list .
# 4.count()             Returns the number of elements with the specified value.
# 5.extend()            Add the element of a list 9or any literable)to the end of the current list.
# 6.index()             return the index of the first element with the specific value.
# 7.insert()            adds an element at the specified position .
# 8.pop()               Removes the element at the specified position .
# 9.remove()            Removes the item with the specified value.
# 10.reverse()          Reverses the order of the list.
# 11.sort()             sorts the list.
