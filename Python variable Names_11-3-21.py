# Rules for python variables:
# 1.variable name can start with a letter or the underscore character.
# 2.it can not start with a number
# 3.it contain alpha-numaric character and underscore(A-z,0-9,and_)
# 4.it is case sensitive (age,Age and AGE are three different variables)
# ______________________________________________________________________________________________________________________
# E.g
# >Legal variable names:
# myvar='john'
# my_var='john'
# _my_var='john'
# myVar='john'
# MYVAR='john'
# myvar2='john'
# ______________________________________________________________________________________________________________________
# >illegal variable names
# 2myvar='swapnil'
# my-var='swapnil'
# my  var='swapnil'
# ______________________________________________________________________________________________________________________
# >Multi words variable names.
# there are several technique you can used to make them more readable.
# Camel case
# Each word ,except the first starts with a capital letter.
# e.g
# myVariableName='swapnil'
# Pascal case
# Each word starts with a capital letter.
# MyVariableName="Swapnil"
# Snake case
# Each word is seperated by an underscore character.
# my_variable_name='swapnil'